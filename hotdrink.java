public class hotdrink {
    
    private String name;
    private int valume;

    public hotdrink(String name, int valume) {
        this.name = name;
        this.valume = valume;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValume() {
        return valume;
    }

    public void setValume(int valume) {
        this.valume = valume;
    }
}
